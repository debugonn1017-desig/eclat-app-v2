// 日付ベースで決定論的に選ぶ（SSR/CSR一致）
export function getTodayQuote(d: Date = new Date()): string {
  // dayOfYear * 1で大きな値 → modで安定したインデックス
  const dayOfYear = Math.floor((d.getTime() - new Date(d.getFullYear(), 0, 0).getTime()) / 86400000)
  const idx = dayOfYear % MANUAL_QUOTES.length
  return MANUAL_QUOTES[idx]!
}

